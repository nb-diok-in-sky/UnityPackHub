export type TranslationKey = keyof typeof translations['zh-CN']

const currentLocale = ref<Locale>('zh-CN')
const t = reactive({ ...translations['zh-CN'] }) as Record<TranslationKey, string>

watchEffect(() => {
  const msgs = translations[currentLocale.value] ?? translations['zh-CN']
  for (const key of Object.keys(msgs) as TranslationKey[]) {
    t[key] = msgs[key]
  }
})

export function useI18n() {
  function setLocale(locale: Locale): void {
    currentLocale.value = locale
  }

  function tr(key: TranslationKey, params?: Record<string, string | number>): string {
    const msgs = translations[currentLocale.value] ?? translations['zh-CN']
    let text: string = msgs[key]
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v))
      }
    }
    return text
  }

  return {
    locale: currentLocale,
    t,
    tr,
    setLocale,
  }
}
